export interface FeatherProps {
  name: FeatherName
  size?: number
  strokeWidth?: number
  className?: string
  style?: CSSProperties
  'aria-hidden'?: boolean
}

export function Feather({
  name,
  size = 16,
  strokeWidth = 2,
  className,
  style,
  'aria-hidden': ariaHidden = true,
}: FeatherProps) {
  const path = PATHS[name]
  if (!path) return null
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      style={style}
      aria-hidden={ariaHidden}
      focusable="false"
    >
      {path}
    </svg>
  )
}
